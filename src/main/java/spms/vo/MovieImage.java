package spms.vo;

public class MovieImage {
	protected String poster;
	protected String age;
	public String getPoster() {
		return poster;
	}
	public MovieImage setPoster(String poster) {
		this.poster = poster;
		return this;
	}
	public String getAge() {
		return age;
	}
	public MovieImage setAge(String age) {
		this.age = age;
		return this;
	}
}
